<script lang="ts" setup>
type PropsType = {

  /** 水印内容 */
  content?: string

  /** 水印是否可见 */
  visible?: boolean

  /** 水印字体大小 */
  fontSize?: number

  /** 水印字体颜色 */
  fontColor?: string

  /** 水印旋转角度 */
  rotate?: number

  /** 水印间距X */
  gapX?: number

  /** 水印间距Y */
  gapY?: number

  /** 水印偏移X */
  offsetX?: number

  /** 水印偏移Y */
  offsetY?: number

  /** 水印层级 */
  zIndex?: number
}

const props = withDefaults(defineProps<PropsType>(), {
  content: 'weiShao',
  visible: false,
  fontSize: 16,
  fontColor: 'rgba(128, 128, 128, 0.2)',
  rotate: -22,
  gapX: 100,
  gapY: 100,
  offsetX: 50,
  offsetY: 50,
  zIndex: 3100,
})

const watermarkVisible = computed(() => props.visible)
</script>

<template>
  <div
    v-if="watermarkVisible"
    class="fixed left-0 top-0 h-[100vh] w-[100vw]"
    :style="{ zIndex }"
  >
    <el-watermark
      :content="content"
      :font="{ fontSize, color: fontColor }"
      :rotate="rotate"
      :gap="[gapX, gapY]"
      :offset="[offsetX, offsetY]"
    >
      <div
        class="h-[100vh]"
      />
    </el-watermark>
  </div>
</template>

